package com.hao.redis.common.constants;

/**
 * 限流阈值常量定义
 *
 * 类职责：
 * 集中管理系统中的限流阈值（QPS），避免魔法数字，便于统一调整与维护。
 *
 * 设计目的：
 * 1. 统一限流阈值口径，确保多处使用一致。
 * 2. 便于压测校准后快速调整系统阈值。
 *
 * 为什么需要该类：
 * 限流阈值是全局配置项，集中管理可避免分散定义带来的维护风险。
 *
 * 核心实现思路：
 * - 使用常量集中声明。
 * - 与注解与配置类配合使用。
 */
public class RateLimitConstants {

    /**
     * 全局服务限流阈值（QPS）
     * 作用：保护整个服务集群不被突发流量打垮，作为系统的最后一道防线。
     * 待完善：以稳定QPS为准，而不是极限QPS。
     * 在不排队、不抖动、不堆积、P95 < 200ms 的情况下的最大QPS。
     * | QPS阈值 | 平均延迟 | P95    | 状态 |
     * | ---- | -------- | ------ | ---- |
     * | 600 | <100ms | <150ms | 稳 |
     * | 800 | <150ms | <200ms | 稳 |
     * | 1000 | <200ms | ~300ms | 轻压 |
     * | 1200 | >500ms | ~800ms | 排队 |
     */
    public static final String GLOBAL_SERVICE_QPS = "2500";

    /**
     * 微博发布接口限流阈值 (QPS)
     * 业务场景：写操作，涉及数据库与Redis多重写入，资源消耗大，阈值设为 10
     */
    public static final String WEIBO_CREATE_QPS = "10";

    /**
     * 私有构造方法
     *
     * 实现逻辑：
     * 1. 禁止实例化常量类。
     */
    private RateLimitConstants() {
        // 实现思路：
        // 1. 阻止外部创建实例。
    }
}
